export default function SelectCountry({ Location, setLocation }) {
  const [query, setQuery] = useState("");
  const [filtered, setFiltered] = useState(COUNTRIES);
  const [ShowDropDown, setShowDropDown] = useState(false);

  useEffect(() => {
    const q = query.toLowerCase();
    setFiltered(COUNTRIES.filter((c) => c.toLowerCase().includes(q)));
  }, [Location]);

  const handleSelectCountry = (country) => {
    setLocation(country);
    setShowDropDown(false);
  };


  return (
    <div className="sm:w-1/2 w-full relative max-w-sm mx-auto">
      <input
        type="text"
        placeholder="Search country..."
        className="w-full py-2 px-5 rounded-2xl border border-[#88358F] active:g-[#88358F] duration-300 transition-all"
        value={Location}
        onChange={(e) => setLocation(e.target.value)}
        onFocus={() => setShowDropDown(true)}
        autoComplete="new-password"
        suggestions={[]}
      />
      {ShowDropDown && (
        <div className="border absolute rounded max-h-48 overflow-auto w-full bg-black text-white shadow">
          {filtered.map((country) => (
            <div
              key={country}
              className={`p-2 cursor-pointer hover:bg-gray-800 ${
                Location === country ? "bg-gray-500 font-semibold" : ""
              }`}
              onClick={() => handleSelectCountry(country)}
            >
              {country}
            </div>
          ))}
          {filtered.length === 0 && (
            <div className="p-2 text-gray-500 text-sm">No country found</div>
          )}
        </div>
      )}
    </div>
  );
}
